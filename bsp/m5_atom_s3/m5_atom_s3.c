esp_err_t bsp_iot_button_create(button_handle_t btn_array[], int *btn_cnt, int btn_array_size)
{
    esp_err_t ret = ESP_OK;
    if ((btn_array_size < BSP_BUTTON_NUM) ||
            (btn_array == NULL)) {
        return ESP_ERR_INVALID_ARG;
    }

    const button_config_t btn_cfg = {0};
    const button_gpio_config_t bsp_press_btn_config = {
        .gpio_num = BSP_BUTTON_PRESS_IO,
        .active_level = 0,
    };
    BSP_ERROR_CHECK_RETURN_NULL(iot_button_new_gpio_device(&btn_cfg, &bsp_press_btn_config, &btn_array[0]));

    if (btn_cnt) {
        *btn_cnt = 1;
    }
    return ret;
}
